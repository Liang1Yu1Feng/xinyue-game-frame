package com.xinyue.web.app.service.model;

public class ResponseResourceMessage {

	private String orgineName;
	private String newName;
	private String url;

	public String getOrgineName() {
		return orgineName;
	}

	public void setOrgineName(String orgineName) {
		this.orgineName = orgineName;
	}

	public String getNewName() {
		return newName;
	}

	public void setNewName(String newName) {
		this.newName = newName;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

}
